public class AverageOfThree {
    public static int averageOfTheree(int num1, int num2, int num3){
        return (num1 + num2 + num3)/3;
    }
    public static void main(String[] args) {
         int n1 = 12;
         int n2 = 345;
         int n3 = 456;
         System.out.println(averageOfTheree(n1, n2, n3));
    }
}
